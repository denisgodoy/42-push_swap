#include	"libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	char	*ptr;

	if (!s1 || !set)
		return (0);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	i = ft_strlen(s1);
	while (i && ft_strchr(set, s1[i]))
		i--;
	ptr = ft_substr(s1, 0, i + 1);
	if (!ptr)
		return (0);
	return (ptr);
}
